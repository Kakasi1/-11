P = float(input("Введите начальную численность населения (P): "))
r = float(input("Введите процентный прирост (r): "))
n = int(input("Введите количество лет (n): "))

print("\nГод\tЧисленность населения")
print("-------------------------")

for year in range(1, n + 1):
    P += P * (r / 100)
    print(f"{year}\t{P:.2f}")